"""
Write a Program in python that takes two numbers as input and check if the first name is divisible by the second.
"""

num1= int(input("Enter number1: "))
num2= int(input("Enter number2: "))

if num1%num2==0:
    print("{num1} is divisible by {num2}")

else:
    print("{num1} is not divisible by {num2}")


